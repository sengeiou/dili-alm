package com.dili.sysadmin.exception;

public class RoleException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3499849320658589342L;

	public RoleException() {
		
	}

	public RoleException(String message) {
		super(message);
		
	}

	public RoleException(Throwable cause) {
		super(cause);
		
	}

	public RoleException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public RoleException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
